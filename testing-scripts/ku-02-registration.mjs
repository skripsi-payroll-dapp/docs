// KU-02 Registration — AU-02-01 s.d. AU-02-12
// Covers: backend/src/routes/registration.ts (POST /request, PATCH /:address/approve, DELETE /:address),
// backend/src/routes/invitations.ts (POST /, GET /:token, GET /hr/:hrAddress, PATCH /:token/revoke),
// and backend/src/routes/auth.ts (POST /auth/profile — this is where NIK format is actually
// validated, NOT in registration.ts; the checklist bundles it under KU-02 conceptually but the
// real validation lives in the profile endpoint).
//
// AU-02-02 and AU-02-06..09 cover the invitation-only employee registration flow: an employee
// can no longer submit a free-typed hrAddress — registration requires a valid, unused,
// unexpired inviteToken generated by HR (fixes the gap where anyone could self-declare
// membership in any company from an unfiltered dropdown — see conversation history / DPPL.md
// for the finding this closes).
//
// Preconditions: TEST_OWNER_PRIVATE_KEY, TEST_HR_PRIVATE_KEY set. AU-02-01 uses a freshly
// generated wallet (no precondition needed — that's the point of testing fresh registration).
import { freshAccount, loginAs, api, assert, section, finish } from "./lib/common.mjs";

section("AU-02-01 — Registrasi company (alamat baru)");
const freshCompany = freshAccount();
{
  const { res, body } = await api("/registration/request", {
    method: "POST",
    body: {
      address: freshCompany.address,
      email: "test-company@example.com",
      name: "PT Uji Otomatis",
      type: "company",
      npwp: "01.234.567.8-901.000",
      nib: "1234567890123",
      directorName: "Budi Tester",
      directorNik: "3201010101010001",
      deedUrl: "https://example.com/deed.pdf",
    },
  });
  assert(res.status === 200 && body.ok === true, "POST /registration/request (company) → ok:true", `status=${res.status} body=${JSON.stringify(body)}`);
}

const hrLoginForInvite = await loginAs("TEST_HR_PRIVATE_KEY");

section("AU-02-06 — HR membuat undangan (invitation) baru");
let inviteToken;
{
  const { res, body } = await api("/invitations", { method: "POST", token: hrLoginForInvite.accessToken, body: { name: "Andi Tester" } });
  assert(res.status === 200 && !!body.token && body.hrAddress?.toLowerCase() === hrLoginForInvite.account.address.toLowerCase(), "POST /invitations → token dibuat, hrAddress benar", `status=${res.status} body=${JSON.stringify(body)}`);
  inviteToken = body.token;
}

section("AU-02-02 — Registrasi employee dengan inviteToken valid");
const freshEmployee = freshAccount();
{
  const { res, body } = await api("/registration/request", {
    method: "POST",
    body: { address: freshEmployee.address, email: "test-emp@example.com", name: "Andi Tester", type: "employee", inviteToken },
  });
  assert(res.status === 200 && body.ok === true, "POST /registration/request (employee, inviteToken valid) → ok:true", `status=${res.status} body=${JSON.stringify(body)}`);
}
{
  // Bab 4 also expects the new request to actually surface in the inviting HR's own pending
  // list — POST returning ok:true alone doesn't prove that row exists with the right hrAddress.
  const { res, body } = await api(`/registration/pending/hr/${hrLoginForInvite.account.address}`, { token: hrLoginForInvite.accessToken });
  const found = Array.isArray(body) && body.some((r) => r.address?.toLowerCase() === freshEmployee.address.toLowerCase());
  assert(res.status === 200 && found, "GET /registration/pending/hr/:hrAddress → berisi request baru ini", `status=${res.status} found=${found}`);
}

section("AU-02-07 — Registrasi employee TANPA inviteToken → 400 (menutup celah pilih-bebas perusahaan)");
{
  const noTokenEmployee = freshAccount();
  const { res, body } = await api("/registration/request", {
    method: "POST",
    body: { address: noTokenEmployee.address, email: "test-emp2@example.com", name: "Tanpa Token", type: "employee" },
  });
  assert(res.status === 400, "POST /registration/request (employee, tanpa inviteToken) → 400", `status=${res.status} body=${JSON.stringify(body)}`);
}

section("AU-02-08 — inviteToken yang sudah dipakai tidak bisa dipakai ulang → 409");
{
  const secondEmployee = freshAccount();
  const { res, body } = await api("/registration/request", {
    method: "POST",
    body: { address: secondEmployee.address, name: "Reuse Token", type: "employee", inviteToken },
  });
  assert(res.status === 409, "Reuse inviteToken yang sudah dipakai → 409", `status=${res.status} body=${JSON.stringify(body)}`);
}

section("AU-02-09 — HR revoke undangan yang belum dipakai, lalu tidak bisa dipakai");
{
  const { body: newInvite } = await api("/invitations", { method: "POST", token: hrLoginForInvite.accessToken, body: {} });
  const { res: revokeRes, body: revokeBody } = await api(`/invitations/${newInvite.token}/revoke`, { method: "PATCH", token: hrLoginForInvite.accessToken });
  assert(revokeRes.status === 200 && revokeBody.ok === true, "PATCH /invitations/:token/revoke → ok:true", `status=${revokeRes.status}`);

  const revokedUseEmployee = freshAccount();
  const { res: useRes, body: useBody } = await api("/registration/request", {
    method: "POST",
    body: { address: revokedUseEmployee.address, name: "Pakai Token Revoked", type: "employee", inviteToken: newInvite.token },
  });
  assert(useRes.status === 409, "Registrasi dengan inviteToken yang sudah di-revoke → 409", `status=${useRes.status} body=${JSON.stringify(useBody)}`);
}

section("AU-02-03 — NIK invalid ditolak (di POST /auth/profile, bukan /registration)");
{
  const employeeLogin = await loginAs("TEST_EMPLOYEE_PRIVATE_KEY");
  if (assert(employeeLogin.ok, "Login precondition for AU-02-03")) {
    const { res, body } = await api("/auth/profile", {
      method: "POST",
      token: employeeLogin.accessToken,
      body: { name: "Test Name", nik: "12345", phone: "081234567890" }, // 5 digits, not 16
    });
    assert(res.status === 400 && body.errorCode === "BAD_REQUEST", "NIK 5 digit → 400 BAD_REQUEST", `status=${res.status} body=${JSON.stringify(body)}`);
  }
}

section("AU-02-04 — Owner approve company");
{
  const ownerLogin = await loginAs("TEST_OWNER_PRIVATE_KEY");
  if (assert(ownerLogin.ok, "Login precondition for AU-02-04 (must match OWNER_ADDRESS env on backend)")) {
    const { res, body } = await api(`/registration/${freshCompany.address}/approve`, { method: "PATCH", token: ownerLogin.accessToken });
    assert(res.status === 200 && body.ok === true, "PATCH /registration/:address/approve (as Owner) → ok:true", `status=${res.status} body=${JSON.stringify(body)}`);
  }
}

section("AU-02-05a — Non-owner mencoba approve registrasi COMPANY → ditolak");
{
  // A second freshly-generated company registration, then TEST_HR (not Owner) tries to
  // approve it — should be forbidden. (Related but distinct code path from AU-02-05b below.)
  const otherCompany = freshAccount();
  await api("/registration/request", {
    method: "POST",
    body: { address: otherCompany.address, name: "PT Lain", type: "company", npwp: "02.345.678.9-012.000" },
  });
  const hrLogin = await loginAs("TEST_HR_PRIVATE_KEY");
  if (assert(hrLogin.ok, "Login precondition for AU-02-05a")) {
    const { res, body } = await api(`/registration/${otherCompany.address}/approve`, { method: "PATCH", token: hrLogin.accessToken });
    assert(res.status === 403, "Non-owner approve attempt (company) → 403 Forbidden", `status=${res.status} body=${JSON.stringify(body)}`);
  }
}

section("AU-02-05b — HR B mencoba approve registrasi EMPLOYEE milik HR A → ditolak (skenario persis Bab 4)");
{
  // freshEmployee's registration (above) has hrAddress = hrLoginForInvite's address (TEST_HR).
  // TEST_LEGAL logs in as an unrelated party (neither Owner nor that employee's HR) and
  // attempts the exact same PATCH /:address/approve the real HR would use.
  const otherPartyLogin = await loginAs("TEST_LEGAL_PRIVATE_KEY");
  if (assert(otherPartyLogin.ok, "Login precondition for AU-02-05b")) {
    const { res, body } = await api(`/registration/${freshEmployee.address}/approve`, { method: "PATCH", token: otherPartyLogin.accessToken });
    assert(res.status === 403, "HR lain (bukan pemilik) approve registrasi employee milik HR A → 403 Forbidden", `status=${res.status} body=${JSON.stringify(body)}`);
  }
}

section("AU-02-10 — NPWP format tidak valid ditolak");
{
  const { res, body } = await api("/registration/request", {
    method: "POST",
    body: { address: freshAccount().address, name: "PT NPWP Invalid", type: "company", npwp: "12345678901234" }, // 14 digit
  });
  assert(res.status === 400, "NPWP 14 digit (bukan 15/16) → 400", `status=${res.status} body=${JSON.stringify(body)}`);
}

section("AU-02-11 — NIB format tidak valid ditolak");
{
  const { res, body } = await api("/registration/request", {
    method: "POST",
    body: { address: freshAccount().address, name: "PT NIB Invalid", type: "company", npwp: "1234567890123456", nib: "123456789012" }, // 12 digit
  });
  assert(res.status === 400, "NIB 12 digit (bukan 13) → 400", `status=${res.status} body=${JSON.stringify(body)}`);
}

section("AU-02-12 — NPWP/NIB format valid diterima (15 & 16 digit NPWP, 13 digit NIB)");
{
  const { res: res15 } = await api("/registration/request", {
    method: "POST",
    body: { address: freshAccount().address, name: "PT NPWP 15", type: "company", npwp: "01.234.567.8-901.000", nib: "1234567890123" },
  });
  assert(res15.status === 200, "NPWP 15 digit (format lama, dengan tanda baca) → 200", `status=${res15.status}`);

  const { res: res16 } = await api("/registration/request", {
    method: "POST",
    body: { address: freshAccount().address, name: "PT NPWP 16", type: "company", npwp: "1234567890123456", nib: "1234567890123" },
  });
  assert(res16.status === 200, "NPWP 16 digit (format baru berbasis NIK) → 200", `status=${res16.status}`);
}

finish();
